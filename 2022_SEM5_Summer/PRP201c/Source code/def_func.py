def print_lyrics():
    print("I'm a lumberjack, and I'm okay.")
    print('I sleep all night and I work all day.')
    
def repeat_lyrics():
    print_lyrics()
    print_lyrics()

repeat_lyrics()


def print_twice(bruce):
    print(bruce)
    print(bruce)    
    
michael = 'Eric, the half a bee.'
print_twice(michael)

