# /	Division
print(12 / 3) # 4.0

# %	Modulus	
print(5 % 2) # 1

# **	Exponentiation
print(2 ** 5) #same as 2*2*2*2*2

# //	Floor division
x = 15
y = 2
print(x // y)
#the floor division // rounds the result down to the nearest whole number

